package org.ietr.preesm.mapper.algo.fast;

import java.util.ArrayList;
import java.util.List;

import org.ietr.dftools.architecture.slam.Design;
import org.ietr.dftools.workflow.WorkflowException;
import org.ietr.preesm.core.scenario.PreesmScenario;
import org.ietr.preesm.mapper.abc.AbcType;
import org.ietr.preesm.mapper.abc.IAbc;
import org.ietr.preesm.mapper.abc.edgescheduling.EdgeSchedType;
import org.ietr.preesm.mapper.abc.impl.latency.InfiniteHomogeneousAbc;
import org.ietr.preesm.mapper.abc.taskscheduling.TopologicalTaskSched;
import org.ietr.preesm.mapper.algo.list.InitialLists;
import org.ietr.preesm.mapper.model.MapperDAG;
import org.ietr.preesm.mapper.params.AbcParameters;
import org.ietr.preesm.mapper.params.FastAlgoParameters;

/**
 * Population for genetic algorithm generated by FAST algorithm
 * 
 * @author pmenuet
 */
public class FastPopulation {

	// Number of individual in the population
	private int populationNum;

	// List of mapperDAG constituting the population
	private List<MapperDAG> population;

	// Simulator used to make this population
	private AbcParameters abcParams;

	// architecture used to make this population
	private Design archi;

	// private IScenario scenario;

	/**
	 * Constructors
	 */

	public FastPopulation() {
		super();

	}

	/**
	 * @param populationNum
	 * @param simulatorType
	 * @param archi
	 */
	public FastPopulation(int populationNum, AbcParameters abcParams,
			Design archi, PreesmScenario scenario) {
		super();
		this.populationNum = populationNum;
		this.abcParams = abcParams;
		this.archi = archi;
		this.population = new ArrayList<MapperDAG>();
		// this.scenario = scenario;
	}

	/**
	 * @param populationNum
	 * @param population
	 * @param simulatorType
	 * @param archi
	 */
	public FastPopulation(int populationNum, List<MapperDAG> population,
			AbcParameters abcParams, Design archi) {
		super();
		this.populationNum = populationNum;
		this.population = population;
		this.abcParams = abcParams;
		this.archi = archi;
	}

	/**
	 * Getters and setters
	 */

	public Design getArchi() {
		return archi;
	}

	public void setArchi(Design archi) {
		this.archi = archi;
	}

	public int getPopulationNum() {
		return populationNum;
	}

	public void setPopulationNum(int populationNum) {
		this.populationNum = populationNum;
	}

	public List<MapperDAG> getPopulation() {
		return population;
	}

	public void setPopulation(List<MapperDAG> population) {
		this.population = population;
	}

	/**
	 * constructPopulation = run the fast algorithm as many times it is
	 * necessary to make the population
	 * 
	 * @param dag
	 * @param MAXCOUNT
	 * @param MAXSTEP
	 * @param MARGIN
	 */
	public void constructPopulation(MapperDAG dag, PreesmScenario scenario,
			FastAlgoParameters fastParams) throws WorkflowException {

		// create the population
		List<MapperDAG> temp = new ArrayList<MapperDAG>();

		// PopulationNum times
		for (int i = 0; i < this.getPopulationNum(); i++) {

			MapperDAG tempdag = null;
			tempdag = dag.clone();

			// perform the initialization
			IAbc simu = new InfiniteHomogeneousAbc(new AbcParameters(
					AbcType.InfiniteHomogeneous, EdgeSchedType.Simple, false),
					tempdag, this.getArchi(), scenario);
			InitialLists initialLists = new InitialLists();
			initialLists.constructInitialLists(tempdag, simu);

			TopologicalTaskSched taskSched = new TopologicalTaskSched(
					simu.getTotalOrder());
			simu.resetDAG();

			// perform the fast algo
			FastAlgorithm algorithm = new FastAlgorithm(initialLists, scenario);
			tempdag = algorithm.map("population", abcParams, fastParams,
					tempdag, this.archi, false, true, false, null, taskSched)
					.clone();
			temp.add(tempdag.clone());

		}
		this.population.addAll(temp);
	}
}
